const fs = require('fs');

// 1. Read student_names.js
const studentNamesContent = fs.readFileSync('c:\\Users\\abens\\OneDrive\\Documents\\02_Projects\\Active_Projects\\Exam Seat\\data\\student_names.js', 'utf8');
// It contains: window.STUDENT_NAMES = { "MG25103A2838": { "name": "AAVANY SUNIL", "gender": "F" }, ... };
// We need to extract the JSON part.
let studentNamesJsonStr = studentNamesContent.replace('window.STUDENT_NAMES = ', '').trim();
if (studentNamesJsonStr.endsWith(';')) {
    studentNamesJsonStr = studentNamesJsonStr.slice(0, -1);
}
const studentNamesObj = JSON.parse(studentNamesJsonStr);

// 2. Read students.json
const adminDataStr = fs.readFileSync('c:\\Users\\abens\\OneDrive\\Documents\\02_Projects\\Active_Projects\\Exam Seat\\MAC-EXAM-Hub-Scraper\\students.json', 'utf8');
const adminDataArray = JSON.parse(adminDataStr); // [{"name": "AAVANY SUNIL", "user": "12727"}, ...]

// 3. Merge them
// We will use Name as the primary key.
const mergedDB = [];
let rollCounter = 1;

// Since both are for BCA A batch, we can assume dept = BCA and classGroup = BCA A.
// We should iterate over the adminDataArray (since it's a clean array of students we know we want to scrape).

adminDataArray.forEach(adminInfo => {
    const name = adminInfo.name.toUpperCase().trim();
    const adminNo = adminInfo.user;

    // Find Reg No from studentNamesObj
    let regNo = "";
    for (const [reg, info] of Object.entries(studentNamesObj)) {
        if (info.name.toUpperCase().trim() === name) {
            regNo = reg;
            break;
        }
    }

    mergedDB.push({
        name: name,
        regNo: regNo,
        adminNo: adminNo,
        rollNo: rollCounter.toString(), // Approximated roll number based on alphabetical order in students.json
        department: "BCA",
        classGroup: "BCA A",
        semester: "Sem 2" // Example semester
    });
    rollCounter++;
});

// Create the final JS file
const fileContent = `// Unified Student Database
// Auto-generated by merge script
window.STUDENTS_DB = ${JSON.stringify(mergedDB, null, 4)};
`;

fs.writeFileSync('c:\\Users\\abens\\OneDrive\\Documents\\02_Projects\\Active_Projects\\Exam Seat\\data\\students_db.js', fileContent);
console.log('Successfully generated data/students_db.js with ' + mergedDB.length + ' students.');
